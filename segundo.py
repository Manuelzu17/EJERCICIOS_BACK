#  The print() function is a built-in function in Python
#  that is used to display text or data to the standard output
#  (typically the screen or console). By default, the print()
#  function separates the arguments it is passed with a space and
#  adds a newline character at the end. Here is an example of how
#  to use the print() function:

print("Hello, World!")

# The print() function is flexible and can take several arguments,
# either separated by commas or as separate arguments. 
# The arguments can be of different data types, including numbers,
# strings, and objects. The print() function automatically 
# converts these data types to strings before printing them:

print("The answer is", 42)

